public class DateClass
{
    private int date;
    private int month;
    private int year;

    public int getDate()
    {
        return date;
    }

    public int getMonth()
    {
        return month;
    }

    public int getYear()
    {
        return year;
    }

    public void setTheDate(int date1,int month1, int year1)
    {
        month = month1;
        date = date1;
        year = year1;

    }
}
